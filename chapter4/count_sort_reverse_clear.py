fruits = ["Apple", "Banana", "Orange", "Apple", "Banana", "Orange", "Apple", "Banana"]

# count the apples
print("Number of apples:", fruits.count("Apple"))

# sort
fruits.sort()
print("Sorted list:", fruits)

# reverse
fruits.reverse()
print("Reversed list:", fruits)

# index
print("1st apple exists in index:", fruits.index("Apple"))

# clear
fruits.clear()
print("Cleared list:", fruits)